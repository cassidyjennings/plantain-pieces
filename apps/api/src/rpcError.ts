/** Maps the exception messages raised by our SQL RPCs to HTTP status codes. */
const KNOWN_ERRORS: Record<string, number> = {
  ROOM_NOT_FOUND: 404,
  NOT_IN_ROOM: 404,
  NOT_A_PLAYER: 403,
  NOT_HOST: 403,
  UNAUTHORIZED: 401,
  GAME_ALREADY_STARTED: 409,
  ALREADY_STARTED: 409,
  ROOM_FULL: 409,
  NEED_TWO_PLAYERS: 409,
  NEED_ONE_PLAYER: 409,
  GAME_NOT_ACTIVE: 409,
  BUNCH_TOO_LOW: 409,
  BUNCH_NOT_LOW: 409,
  STALE_ACTION: 409,
  TILE_NOT_HELD: 400,
  NOT_FOUND: 404,
  INVALID_NAME: 400,
  INVALID_WORD_SET: 400,
  TOO_MANY_SETS: 409,
  TOO_MANY_WORDS: 409,
  TOO_MANY_PRESETS: 409,
  INVALID_DICTIONARY_CONFIG: 400,
  NO_WORD_SOURCE: 400,
  INVALID_CUSTOM_SET: 400,
  // Profile / stats
  EMPTY: 400,
  TOO_LONG: 400,
  INVALID_CHARS: 400,
  INVALID_AVATAR_CONFIG: 400,
  INVALID_SUMMARY: 400,
  PROFILE_NOT_FOUND: 404,
  NOT_A_PARTICIPANT: 404,
};

export function statusForRpcError(message: string): 400 | 401 | 403 | 404 | 409 {
  return (KNOWN_ERRORS[message] as 400 | 401 | 403 | 404 | 409 | undefined) ?? 400;
}
